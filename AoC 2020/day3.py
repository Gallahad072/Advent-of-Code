def getTreeCollisions(inpt, x_step, y_step):
    x, y = 0, 0
    collisions = 0
    while y < len(inpt):
        if inpt[y][x % 31] == "#":
            collisions += 1
        x += x_step
        y += y_step

    return collisions


# Part 2
def getSlopesCollisionsMultiplied(inpt):
    slopes_multiplied = 1
    slopes = [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)]
    for slope in slopes:
        slopes_multiplied *= getTreeCollisions(inpt, slope[0], slope[1])

    return slopes_multiplied


